    public NguoiDung DoiMatKhau(NguoiDung nd) {
        PreparedStatement ps = null;
        if (DBConnect.open()) {
            try {
                ps = DBConnect.cnn.prepareStatement("update tblNguoiDung set fldMatKhau = ? where fldTenDangNhap = ?");
               
                ps.setString(1, nd.getMatKhau());
                ps.setString(2, nd.getTenDangNhap());
                        
                int row = ps.executeUpdate();
                if (row < 1) {
                    nd = null;
                }
            } catch (SQLException ex) {
               
                nd = null;
            } finally {
                DBConnect.close();
            }
        }
        return nd;
    }

                                     
