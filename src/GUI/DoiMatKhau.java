public ArrayList<NguoiDung> findNguoiDung(String TenDangNhap) {
        ArrayList<NguoiDung> list = null;
        PreparedStatement ps = null;
        ResultSet rs = null;
        if (DBConnect.open()) {
            try {
                ps = DBConnect.cnn.prepareStatement("select * from tblNguoiDung where fldTenDangNhap = ?");
               ps.setString(1, MainAdmin.nameLogin);
                rs = ps.executeQuery();
                list = new ArrayList<NguoiDung>();
                while (rs.next()) {
                    NguoiDung nd = new NguoiDung();
                    nd.setTenDangNhap(rs.getString(1));
                    nd.setMatKhau(rs.getString(2));
                    
                    list.add(nd);
                }

            } catch (SQLException ex) {
               
            } finally {
                DBConnect.close(ps, rs);
            }
        }
        return list;
    }

