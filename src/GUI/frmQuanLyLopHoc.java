    private void jbNewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbNewActionPerformed
        String malop = txtMaLop.getText();
        String tenlop = txtTenLop.getText();
        String siso = txtSiso.getText();
        String maChuyenNganh = allChuyenNganh.get(jcbMaChuyenNganh.getSelectedIndex()).getMaChuyenNganh();
        String khoahoc = txtKhoaHocBD.getText()+"-"+txtKhoaHocKT.getText();
        if (!checkinfo()) {
            return;
        }

        LopHoc lh=new LopHoc(malop, tenlop, maChuyenNganh, siso, khoahoc);
        ArrayList<LopHoc> listCheck = new LopHocDAO().checkID(malop);
        if (listCheck.size() > 0) {
            JOptionPane.showMessageDialog(this, "Mã ID bị trùng", "vui lòng kiểm tra lại", JOptionPane.ERROR_MESSAGE);
            txtMaLop.setText("");
            txtMaLop.requestFocus();
        } else {
            LopHoc insert = new LopHocDAO().addNew(lh);
            JOptionPane.showMessageDialog(this, "Thêm thành công !", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            if (insert != null) {
                showAll();
            }
        }

    }//GEN-LAST:event_jbNewActionPerformed

    private void jbDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbDeleteActionPerformed
        int b = JOptionPane.showConfirmDialog(null, "Bạn chắc chắn muốn xóa dữ liệu này?", "Thông Báo", JOptionPane.YES_NO_OPTION);
        if (b == JOptionPane.YES_OPTION) {
            try {
                String malop = txtMaLop.getText();
                new LopHocDAO().deleteLopHoc(malop);
                JOptionPane.showMessageDialog(this, "Xóa thành công !", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            }catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Dữ liệu chưa thể xóa, lớp học còn sinh viên, hãy xóa dữ liệu sinh viên trước", "Thông báo", JOptionPane.ERROR_MESSAGE);
            } catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(this, "Dữ liệu chưa thể xóa, lớp học còn sinh viên, hãy xóa dữ liệu sinh viên trước", "Thông báo", JOptionPane.ERROR_MESSAGE);
            }
            while (dtm.getRowCount() > 0) {
                dtm.removeRow(0);
            }
            loaddata();
            ResetForm();
            jbNew.setEnabled(true);
            jbUpdate.setEnabled(false);
            jbDelete.setEnabled(false);
            txtMaLop.setEnabled(true);
        }
    }//GEN-LAST:event_jbDeleteActionPerformed
