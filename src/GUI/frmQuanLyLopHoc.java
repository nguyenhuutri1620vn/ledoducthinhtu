    private void showAll() {
        while (dtm.getRowCount() > 0) {
            dtm.removeRow(0);
        }
        int selectedIndex = jcbMaChuyenNganh.getSelectedIndex();
        ChuyenNganh getChuyenNganh = allChuyenNganh.get(selectedIndex);
        ArrayList<LopHoc> lhs = new LopHocDAO().getAll();
        for (LopHoc lopHoc : lhs) {
            Vector v = new Vector();
            v.add(lopHoc.getMalop());
            v.add(lopHoc.getTenlop());
            v.add(lopHoc.getMaChuyenNganh());
            v.add(lopHoc.getSiSo());
            v.add(lopHoc.getKhoahoc());
            dtm.addRow(v);
        }
        ResetForm();
    }
