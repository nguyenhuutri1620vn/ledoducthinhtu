    public boolean checkinfo() {
        Check c = new Check();
        if (!c.checkID(txtMaLop.getText())) {
            JOptionPane.showMessageDialog(this, "Mã lớp phải lớn hơn 2 ký tự,không được trống và có khoảng trắng !", "Hãy nhập lại", JOptionPane.ERROR_MESSAGE);
            txtMaLop.setText("");
            txtMaLop.requestFocus();
            return false;
        } else if (!c.checkSpace(txtTenLop.getText()) || !c.check(txtTenLop.getText())) {
            JOptionPane.showMessageDialog(this, "Tên lớp không được trống !", "Hãy nhập lại", JOptionPane.ERROR_MESSAGE);
            txtTenLop.setText("");
            txtTenLop.requestFocus();
            return false;

        } else if (!c.checkSpace(txtSiso.getText()) || !c.checkNumber(txtSiso.getText())||Integer.parseInt(txtSiso.getText())>100||Integer.parseInt(txtSiso.getText())<40) {
            JOptionPane.showMessageDialog(this, "Sĩ Số phải là số tự nhiên nhỏ hơn 100 và lớn hơn 40", "Hãy nhập lại", JOptionPane.ERROR_MESSAGE);
            txtSiso.setText("");
            txtSiso.requestFocus();
            return false;
        }else if (!c.check(txtKhoaHocBD.getText()) || !c.check(txtKhoaHocKT.getText())
                ||Integer.parseInt(txtKhoaHocBD.getText())>Integer.parseInt(txtKhoaHocKT.getText())
                ||Integer.parseInt(txtKhoaHocKT.getText())-Integer.parseInt(txtKhoaHocBD.getText())>4
                ||Integer.parseInt(txtKhoaHocKT.getText())-Integer.parseInt(txtKhoaHocBD.getText())<3
                ||txtKhoaHocBD.getText().length()<4
                ||txtKhoaHocKT.getText().length()<4
                ||Integer.parseInt(txtKhoaHocBD.getText())<2016) {
            JOptionPane.showMessageDialog(this, "Khóa học không được trống(năm học phải là số từ 2016 đến 3000, năm học bắt đầu phải nhỏ hơn năm học học kết thúc ít nhất 3 năm và nhiều nhất 4 năm)", "Hãy nhập lại", JOptionPane.ERROR_MESSAGE);
            txtKhoaHocBD.setText("");
            txtKhoaHocKT.setText("");
            txtKhoaHocBD.requestFocus();
            return false;

        }
        
        return true;
    }
